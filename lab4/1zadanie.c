#include <stdio.h>

// Задание 13: Проверить, является ли натуральное число n степенью числа 2
// Степень двойки имеет ровно один единичный бит: n & (n-1) == 0

int main()
{
    int n;
    printf("Введите натуральное число n: ");
    scanf("%d", &n);

    if (n <= 0) {
        printf("Ошибка: число должно быть натуральным (n > 0)\n");
        return 1;
    }

    if ((n & (n - 1)) == 0)
        printf("%d является степенью числа 2\n", n);
    else
        printf("%d не является степенью числа 2\n", n);

    return 0;
}
